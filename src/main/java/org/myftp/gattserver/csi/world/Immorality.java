package org.myftp.gattserver.csi.world;

/**
 * Stupnì nemorálnosti
 * 
 * @author Hynek
 * 
 */
public enum Immorality {

	/**
	 * Morální vztah - rodiná pøíslušnost, zamìstnání, spoluák apod.
	 */
	MORAL,

	/**
	 * Lehká nemorálnost - osoby se nemají moc rádi (pomluva, tolerující
	 * manelství, poniování, dlouhodobé sváry, sok v lásce)
	 */
	SLIGHTLY_IMMORAL,

	/**
	 * Znatelná nemorálnost - nevìra, kráde, velké dluhy
	 */
	IMMORAL,

	/**
	 * Tìká nemorálnost - vydírání, tırání, vrada, šikana, znásilnìní;
	 */
	VERY_IMMORAL,
	
	/**
	 * Speciální (vrah, obì, nálezce obìti)
	 */
	SPECIAL;

}
